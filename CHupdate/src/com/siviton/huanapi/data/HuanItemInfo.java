package com.siviton.huanapi.data;

public class HuanItemInfo implements Cloneable {
	private int id = 0;
	private String deviceid;
	private String dnum;
	private String devicemodel;
	private String activekey;
	private String didtoken;
	private String token;
	private String huanid;
	private String licensetype;
	private String licensedata;
	private String downloadid;
	private String updatestate;

	public void setId(int Id) {
		id = Id;
	}

	public int getId() {
		return id;
	}

	public String getDeviceid() {
		return deviceid;
	}

	public void setDeviceid(String deviceid) {
		this.deviceid = deviceid;
	}

	public String getDnum() {
		return dnum;
	}

	public void setDnum(String dnum) {
		this.dnum = dnum;
	}

	public String getDevicemodel() {
		return devicemodel;
	}

	public void setDevicemodel(String devicemodel) {
		this.devicemodel = devicemodel;
	}

	public String getActivekey() {
		return activekey;
	}

	public void setActivekey(String activekey) {
		this.activekey = activekey;
	}

	public String getDidtoken() {
		return didtoken;
	}

	public void setDidtoken(String didtoken) {
		this.didtoken = didtoken;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}

	public String getHuanid() {
		return huanid;
	}

	public void setHuanid(String huanid) {
		this.huanid = huanid;
	}

	public String getLicensetype() {
		return licensetype;
	}

	public void setLicensetype(String licensetype) {
		this.licensetype = licensetype;
	}

	public String getLicensedata() {
		return licensedata;
	}

	public void setLicensedata(String licensedata) {
		this.licensedata = licensedata;
	}

	public String getDownloadid() {
		return downloadid;
	}

	public void setDownloadid(String downloadid) {
		this.downloadid = downloadid;
	}

	public String getUpdatestate() {
		return updatestate;
	}

	public void setUpdatestate(String updatestate) {
		this.updatestate = updatestate;
	}

}
