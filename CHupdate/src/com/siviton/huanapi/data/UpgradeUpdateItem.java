package com.siviton.huanapi.data;

public class UpgradeUpdateItem {
	private String type;
	private String appid;
	private String apptype;
	private String title;
	private String version;
	private String verid;
	private String description;
	private String minicon;
	private String midicon;
	private String note;

	public UpgradeUpdateItem(String type, String appid, String apptype,
			String title, String version, String verid, String description,
			String minicon, String midicon, String note) {
		super();
		this.type = type;
		this.appid = appid;
		this.apptype = apptype;
		this.title = title;
		this.version = version;
		this.verid = verid;
		this.description = description;
		this.minicon = minicon;
		this.midicon = midicon;
		this.note = note;
	}

	public UpgradeUpdateItem() {
		super();
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getAppid() {
		return appid;
	}

	public void setAppid(String appid) {
		this.appid = appid;
	}

	public String getApptype() {
		return apptype;
	}

	public void setApptype(String apptype) {
		this.apptype = apptype;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public String getVerid() {
		return verid;
	}

	public void setVerid(String verid) {
		this.verid = verid;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getMinicon() {
		return minicon;
	}

	public void setMinicon(String minicon) {
		this.minicon = minicon;
	}

	public String getMidicon() {
		return midicon;
	}

	public void setMidicon(String midicon) {
		this.midicon = midicon;
	}

	public String getNote() {
		return note;
	}

	public void setNote(String note) {
		this.note = note;
	}

}
